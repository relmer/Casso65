#include "../CassoEmuCore/Pch.h"

#include "MockHostShell.h"





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::MockHostShell
//
////////////////////////////////////////////////////////////////////////////////

MockHostShell::MockHostShell ()
{
}





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::OpenWindow
//
//  No real window is ever created. Just records the requested geometry.
//
////////////////////////////////////////////////////////////////////////////////

HRESULT MockHostShell::OpenWindow (int w, int h)
{
    m_windowWidth  = w;
    m_windowHeight = h;
    m_windowOpened = true;

    return S_OK;
}





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::PresentFrame
//
////////////////////////////////////////////////////////////////////////////////

HRESULT MockHostShell::PresentFrame (const Framebuffer & fb)
{
    UNREFERENCED_PARAMETER (fb);

    m_presentCount += 1;

    return S_OK;
}





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::OpenAudioDevice
//
////////////////////////////////////////////////////////////////////////////////

HRESULT MockHostShell::OpenAudioDevice (IAudioSink * & outSink)
{
    outSink = &m_audioSink;

    return S_OK;
}





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::PollInput
//
////////////////////////////////////////////////////////////////////////////////

HRESULT MockHostShell::PollInput (std::vector<InputEvent> & outEvents)
{
    outEvents = m_pendingEvents;
    m_pendingEvents.clear ();

    return S_OK;
}





////////////////////////////////////////////////////////////////////////////////
//
//  MockHostShell::InjectEvent
//
////////////////////////////////////////////////////////////////////////////////

void MockHostShell::InjectEvent (const InputEvent & evt)
{
    m_pendingEvents.push_back (evt);
}
