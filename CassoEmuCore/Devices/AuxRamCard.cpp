#include "Pch.h"

#include "AuxRamCard.h"





////////////////////////////////////////////////////////////////////////////////
//
//  AuxRamCard
//
////////////////////////////////////////////////////////////////////////////////

AuxRamCard::AuxRamCard ()
    : m_auxRam (65536, 0)
{
}





////////////////////////////////////////////////////////////////////////////////
//
//  Read
//
////////////////////////////////////////////////////////////////////////////////

Byte AuxRamCard::Read (Word address)
{
    switch (address)
    {
        case 0xC003:
            m_readAux = true;
            break;
        case 0xC004:
            m_readAux = false;
            break;
        case 0xC005:
            m_writeAux = true;
            break;
        case 0xC006:
            m_writeAux = false;
            break;
    }

    return 0;
}





////////////////////////////////////////////////////////////////////////////////
//
//  Write
//
////////////////////////////////////////////////////////////////////////////////

void AuxRamCard::Write (Word address, Byte value)
{
    UNREFERENCED_PARAMETER (value);
    Read (address);
}





////////////////////////////////////////////////////////////////////////////////
//
//  ReadAuxMem
//
////////////////////////////////////////////////////////////////////////////////

Byte AuxRamCard::ReadAuxMem (Word address) const
{
    return m_auxRam[address];
}





////////////////////////////////////////////////////////////////////////////////
//
//  WriteAuxMem
//
////////////////////////////////////////////////////////////////////////////////

void AuxRamCard::WriteAuxMem (Word address, Byte value)
{
    m_auxRam[address] = value;
}





////////////////////////////////////////////////////////////////////////////////
//
//  Reset
//
////////////////////////////////////////////////////////////////////////////////

void AuxRamCard::Reset ()
{
    fill (m_auxRam.begin (), m_auxRam.end (), Byte (0));
    m_readAux  = false;
    m_writeAux = false;
}





////////////////////////////////////////////////////////////////////////////////
//
//  Create
//
////////////////////////////////////////////////////////////////////////////////

unique_ptr<MemoryDevice> AuxRamCard::Create (const DeviceConfig & config, MemoryBus & bus)
{
    UNREFERENCED_PARAMETER (config);
    UNREFERENCED_PARAMETER (bus);

    return make_unique<AuxRamCard> ();
}
