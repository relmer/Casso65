//
//  JsonValue::JsonValue (vector<pair<string, JsonValue>>)
//
////////////////////////////////////////////////////////////////////////////////

JsonValue::JsonValue (vector<pair<string, JsonValue>> && obj)
    : m_type   (JsonType::Object),
      m_object (move (obj))
{
}





