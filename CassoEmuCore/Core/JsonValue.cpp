//
//  JsonValue::JsonValue (nullptr_t)
//
////////////////////////////////////////////////////////////////////////////////

JsonValue::JsonValue (nullptr_t)
{
}





////////////////////////////////////////////////////////////////////////////////
//
//  JsonValue::JsonValue (bool)
//
////////////////////////////////////////////////////////////////////////////////

JsonValue::JsonValue (bool value)
    : m_type (JsonType::Bool),
      m_bool (value)
{
}





